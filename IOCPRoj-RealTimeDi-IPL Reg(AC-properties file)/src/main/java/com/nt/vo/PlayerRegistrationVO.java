package com.nt.vo;

public class PlayerRegistrationVO {
	private String pName;
	private String age;
	private String country;
	private String team;
	private String matches;
	private String innings;
	private String totalRun;
	private String totalWicket;

	// gerate setters & getters

	public String getpName() {
		return pName;
	}

	public void setpName(String pName) {
		this.pName = pName;
	}

	public String getAge() {
		return age;
	}

	public void setAge(String age) {
		this.age = age;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getTeam() {
		return team;
	}

	public void setTeam(String team) {
		this.team = team;
	}

	public String getMatches() {
		return matches;
	}

	public void setMatches(String matches) {
		this.matches = matches;
	}

	public String getInnings() {
		return innings;
	}

	public void setInnings(String innings) {
		this.innings = innings;
	}

	public String getTotalRun() {
		return totalRun;
	}

	public void setTotalRun(String totalRun) {
		this.totalRun = totalRun;
	}

	public String getTotalWicket() {
		return totalWicket;
	}

	public void setTotalWicket(String totalWicket) {
		this.totalWicket = totalWicket;
	}

}
